package com.wsria.arch.util.im.msn;

/**
 * MSN相关异常
 *
 * @author HenryYan
 *
 */
public class MsnException extends Exception {

	private static final long serialVersionUID = 1L;

	public MsnException() {
		super();
	}

	public MsnException(String message, Throwable cause) {
		super(message, cause);
	}

	public MsnException(String message) {
		super(message);
	}

	public MsnException(Throwable cause) {
		super(cause);
	}

}
